    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // PRINT BUTTON
        jTextArea1.setText(""); //Clear previous output
        
        //Checking if the user has saved their input (count will increment)
        if (count == 0) {
            jTextArea1.setText("No inspections to display.");
            return;
        }
        
        for (int i = 0; i < count; i++) {
            jTextArea1.append("Hospital Name: " + inspections[i].getName() + "\n");
            jTextArea1.append("Hospital Location: " + inspections[i].getLocation() + "\n");
            jTextArea1.append("Years Since Inspection: " + inspections[i].getYears() + "\n");
            jTextArea1.append("*".repeat(40) + "\n");
//            jTextArea1.append("**************\n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

