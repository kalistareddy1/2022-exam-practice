    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // SAVE DROPDOWN
        //Checking if the user has reached the maximum number of allowed insoections before saving input
        if (count >= inspections.length) {
            JOptionPane.showMessageDialog(null,
            "Maximum inspection count reached!", "Message", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        //Declaring input in variables
        String name = jTextField1.getText().trim(); //Removing extra spaces after using trim
        String location = jTextField2.getText().trim();
        String years = jComboBox1.getSelectedItem().toString(); //Converting to String value for display
        
        //Checking that all fields are filled before saving input
        if (name.isEmpty() || location.isEmpty()) {
            JOptionPane.showMessageDialog(null,
            "Please fill in all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //Storing user input values to array
        inspections[count] = new Hospital_Inspection(name, location, years);
        count++;
        
        //Feedback message once the input has been successfully stored in the array
        JOptionPane.showMessageDialog(null,
            "Inspection saved!", "Message", JOptionPane.INFORMATION_MESSAGE);
        
        //Calling method to reset the input fields
        clearFields();
    }//GEN-LAST:event_jMenuItem1ActionPerformed

